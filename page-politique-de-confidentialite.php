    <strong>I- Politique de confidentialité</strong>

    <p>Cette politique de confidentialité explique comment nous recueillons, utilisons, divulguons et protégeons les
        informations personnelles des utilisateurs sur notre site de rencontres. Nous nous engageons à respecter votre
        vie privée et à protéger les informations que vous nous fournissez. Veuillez lire attentivement cette politique
        de confidentialité pour comprendre comment nous traitons vos informations personnelles.</p>

    1. Informations collectées : <br> </p>
    Nous pouvons collecter les informations personnelles suivantes lorsque vous utilisez notre site de rencontres :<br>
    - Informations d'inscription : nom, adresse e-mail, date de naissance, sexe, préférences.<br>
    - Informations de profil : photos, description, intérêts, hobbies.<br>
    - Informations de communication : messages, discussions, correspondances.<br>
    - Informations de localisation : adresse IP, géolocalisation approximative. <br> </p>

    2. Utilisation des informations :<br></p>
    Nous utilisons les informations personnelles collectées pour les finalités suivantes :<br>
    - Fournir et améliorer nos services de rencontres.<br>
    - Personnaliser votre expérience utilisateur et afficher des correspondances appropriées.<br>
    - Communiquer avec vous concernant votre compte, les notifications et les mises à jour.<br>
    - Analyser les tendances et les comportements des utilisateurs pour améliorer nos services.<br></p>

    3. Divulgation des informations :<br></p>
    Nous ne divulguerons pas vos informations personnelles à des tiers sans votre consentement, sauf dans les cas
    suivants :<br>
    - Si cela est requis par la loi, une ordonnance d'un tribunal ou une demande des autorités compétentes.<br>
    - Pour protéger nos droits, notre sécurité ou notre propriété intellectuelle.<br>
    - Dans le cadre d'une fusion, acquisition ou vente d'actifs, où les informations peuvent être transférées à des
    tiers impliqués.<br></p>

    4. Sécurité des informations :<br></p>
    Nous mettons en place des mesures de sécurité appropriées pour protéger vos informations personnelles contre tout
    accès non autorisé, utilisation abusive, divulgation ou destruction. Cependant, aucun système de transmission ou de
    stockage de données n'est totalement sécurisé, et nous ne pouvons garantir la sécurité absolue de vos
    informations.<br></p>

    5. Cookies et technologies similaires :<br></p>
    Nous utilisons des cookies et d'autres technologies similaires pour améliorer votre expérience sur notre site et
    collecter des informations analytiques sur l'utilisation du site. Vous pouvez contrôler les cookies dans les
    paramètres de votre navigateur, mais veuillez noter que cela peut affecter certaines fonctionnalités de notre
    site.<br></p>

    6. Modifications de la politique de confidentialité :<br></p>
    Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Toute modification sera
    affichée sur cette page avec une nouvelle date d'entrée en vigueur. Nous vous encourageons à consulter régulièrement
    cette politique pour rester informé des pratiques en matière de confidentialité.<br></p>

    Si vous avez des questions ou des préoccupations concernant notre politique de confidentialité, veuillez nous
    contacter via les coordonnées fournies sur notre site.<br></p><br><br>


